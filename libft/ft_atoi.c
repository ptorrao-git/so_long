#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	neg;
	int	t;

	i = 0;
	neg = 1;
	t = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		t = t * 10 + (str[i] - '0');
		i++;
	}
	t = t * neg;
	return (t);
}
